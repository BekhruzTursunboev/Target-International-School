// Function to get current language
function getCurrentLanguage() {
  return localStorage.getItem("language") || "en";
}

// Function to set language
function setLanguage(lang) {
  console.log("Setting language to:", lang);
  if (translations[lang]) {
    localStorage.setItem("language", lang);
    console.log("Language saved to localStorage");

    // Update all language links
    document.querySelectorAll(".lang-link").forEach((link) => {
      if (link.getAttribute("data-lang") === lang) {
        link.classList.add("active");
      } else {
        link.classList.remove("active");
      }
    });

    // Update page content
    updatePageContent();
  } else {
    console.error("Invalid language:", lang);
  }
}

// Function to translate text
function translate(key) {
  const lang = getCurrentLanguage();
  const translation = translations[lang][key] || translations["en"][key] || key;
  console.log(`Translating ${key} to ${lang}:`, translation);
  return translation;
}

// Function to update all translatable elements
function updatePageContent() {
  const lang = getCurrentLanguage();
  console.log("Updating page content to language:", lang);

  // Update all elements with data-translate attribute
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate");
    const translation = translations[lang][key];
    console.log(`Updating element with key ${key}:`, translation);

    if (translation) {
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        element.placeholder = translation;
      } else if (element.tagName === "IMG") {
        element.alt = translation;
      } else {
        element.textContent = translation;
      }
    } else {
      console.warn(`No translation found for key: ${key} in language: ${lang}`);
    }
  });

  // Update page title if it has data-translate attribute
  const pageTitle = document.querySelector("title[data-translate]");
  if (pageTitle) {
    document.title = translate(pageTitle.getAttribute("data-translate"));
  }

  // Update meta description if it has data-translate attribute
  const metaDescription = document.querySelector(
    'meta[name="description"][data-translate]'
  );
  if (metaDescription) {
    metaDescription.setAttribute(
      "content",
      translate(metaDescription.getAttribute("data-translate"))
    );
  }
}

// Initialize language system when DOM is loaded
document.addEventListener("DOMContentLoaded", () => {
  // Set initial language
  const currentLang = getCurrentLanguage();

  // Update active state of language links
  document.querySelectorAll(".lang-link").forEach((link) => {
    if (link.getAttribute("data-lang") === currentLang) {
      link.classList.add("active");
    } else {
      link.classList.remove("active");
    }
  });

  // Update page content with current language
  updatePageContent();
});
